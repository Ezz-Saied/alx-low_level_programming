#include "3-calc.h"
/**
* op_add - sum two numbers
* @a: first operand
* @b: second operand
*
* Return: sum
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
* op_sub - sub two numbers
* @a: first operand
* @b: second operand
*
* Return: return sub of two numbers
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
* op_mul - mul of two numbers
* @a: first operand
* @b: second operand
*
* Return: mul of two numbers
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
* op_div - div two numbers
* @a: first operand
* @b: second operand
*
* Return: div of two numbers
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - mod func
* @a: first operand
* @b: second operand
*
* Return: remainder of mod two numbers
*/
int op_mod(int a, int b)
{
	return (a % b);
}
